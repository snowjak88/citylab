package org.snowjak.city.map.tileset;

public class TilesetException extends Exception {
	
	private static final long serialVersionUID = 8836761583752342300L;
	
	public TilesetException() {
		
		super();
	}
	
	public TilesetException(String message, Throwable cause) {
		
		super(message, cause);
	}
	
	public TilesetException(String message) {
		
		super(message);
	}
	
	public TilesetException(Throwable cause) {
		
		super(cause);
	}
}